                <p class="card-text">Benteng Otanaha merupakan saksi sejarah dari perjuangan masyarakat Gorontalo pada saat itu ketika berperang melawan Portugis. Salah satu obyek wisata Kota Gorontalo ini terletak di Kelurahan Dembe I, Kecamatan Kota Barat dan letaknya bersebelahan dengan Danau Limboto yang merupakan salah satunya danau di Gorontalo.

                  Benteng Otanaha mempunyai tiga bangunan yang masing-masing diberi nama yaitu Otanaha sebagai benteng utama, Otahiya dan Ulupahu untuk benteng dua lainnya.

                  Otanaha sendiri berasal dari bahasa setempat dimana Ota yang mempunyai arti benteng dan Naha yang merupakan nama anak dari Raja Ilato yang menemukan kembali benteng ini.

                  Benteng ini dahulu dibuat atas kesepakatan antara Raja Ilato dan Bangsa Portugis yang saat itu tiba di Gorontalo. Benteng dibuat dengan tujuan sebagai benteng pertahanan dari serangan musuh.

                  Namun, Portugis pada akhirnya tidak menganggap kesepakatan tersebut dan berniat mengambil alih kekuasaan di Gorontalo.

                  Raja Ilato beserta masyarakat Gorontalo pun berperang melawan Portugis untuk mempertahankan wilayah Gorontalo. Setelah Portugis berhasil diusir dari Gorontalo, benteng ini digunakan sebagai benteng pertahanan oleh raja-raja selanjutnya.

                  Untuk mencapai benteng Otanaha, pengunjung harus mendaki 351 anak tangga. Namun tak perlu khawatir kelelahan, karena terdapat empat titik pesinggahan yang dapat digunakan untuk beristirahat sejenak menghirup udara segar perbukitan dan Danau Limboto.

                  Jika malas menaiki anak tangga, pengunjung dapat menggunakan sepeda motor atau mobil hingga area parkir yang terletak tepat di bawah benteng utama.

                  Hal unik lainnya yang terdapat di benteng ini adalah pada proses pembuatannya, batu-batu yang menjadi material utama untuk membangun benteng ini direkatkan dengan putih telur burung Maleo yang merupakan burung endemic Sulawesi.

                  Benteng Otanaha berjarak sekitar 8 km dari pusat Kota Gorontalo. Di tepi Danau Limboto, terdapat Museum Pendaratan Pesawat Amfibi Soekarno yang dapat dikunjungi setelah puas menikmati pemandangan sekitar Benteng Otanaha.

                  Museum ini berjarak sekitar 2 km dari Benteng Otanaha. Selain sebagai obyek wisata sejarah, Benteng Otanaha juga menawarkan spot-spot yang bagus untuk para pengunjung yang gemar berfoto dengan latar Danau Limboto.</p>
